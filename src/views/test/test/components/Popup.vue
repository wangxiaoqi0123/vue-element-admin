<template>
  <div
    v-show="value"
    class="vue-image-crop-upload"
  >
    <div class="vicp-wrap">
      <div
        class="vicp-close"
        @click="off"
      >
        <i class="vicp-icon4" />
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    // 显示该控件与否
    value: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
    }
  },
  created() {
    // 绑定按键esc隐藏此插件事件
    document.addEventListener('keyup', this.closeHandler)
  },
  destroyed() {
    document.removeEventListener('keyup', this.closeHandler)
  },
  methods: {
    // 关闭控件
    off() {
      setTimeout(() => {
        this.$emit('input', false)
        this.$emit('close')
      }, 200)
    },
    closeHandler(e) {
      if (this.value && (e.key === 'Escape' || e.keyCode === 27)) {
        this.off()
      }
    }
  }
}
</script>

